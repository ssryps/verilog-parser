    bgezal $3, s3           # 20:$31=0x0000010c
    or   $1, $0, $31        # 21:$1=0x0000010c
    ori  $1, $0, 0x000b     # 22:$1=0x0000000b
    ori  $1, $0, 0x000c     # 23:$1=0x0000000c
    ori  $1, $0, 0x000d     # 24:$1=0x0000000d
    ori  $1, $0, 0x000e     # 25:$1=0x0000000e
    bltz $3, s5             # 26:skip
    ori  $1, $0, 0x000f     # 27:$1=0x0000000f
    ori  $1, $0, 0x1100

    .org 0x130
s5:
    ori  $1, $0, 0x0010     # 28:$1=0x00000010
    blez $1, 2b             # 29:skip
    ori  $1, $0, 0x0011     # 30:$1=0x00000011
    ori  $1, $0, 0x0012     # 31:$1=0x00000012

    ori  $1, $0, 0x0013     # 32:$1=0x00000013
    bltzal $3, s6           # 33:$31=0x0000014c
    or   $1, $0, $31        # 34:$1=0x0000014c
    ori  $1, $0, 0x1100

    .org 0x160
