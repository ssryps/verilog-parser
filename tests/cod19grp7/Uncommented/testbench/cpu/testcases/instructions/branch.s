    ori  $1, $0, 0x0002     # 5:$1=0x00000002
1:
    ori  $1, $0, 0x1111
    ori  $1, $0, 0x1100

    .org 0x20
s1:
    ori  $1, $0, 0x0003     # 6:$1=0x00000003
    bal  s2                 # 7:$31=0x0000002c
    nop                     # 8:$0=0x00000000
    ori  $1, $0, 0x1100
    ori  $1, $0, 0x1111
    bne  $1, $0, s3
    
    nop
    ori  $1, $0, 0x1100
    ori  $1, $0, 0x1111

    .org 0x50
s2:
    ori  $1, $0, 0x0004     # 9:$1=0x00000004
    beq  $3, $3, s3         # 10:skip
    or   $1, $31, $0        # 11:$1=0x0000002c
    ori  $1, $0, 0x1111
    ori  $1, $0, 0x1100
2:      
    ori  $1, $0, 0x0007     # 15:$1=0x00000007
    ori  $1, $0, 0x0008     # 16:$1=0x00000008
    bgtz $1, s4             # 17:skip
    ori  $1, $0, 0x0009     # 18:$1=0x00000009
    ori  $1, $0, 0x1111
    ori  $1, $0, 0x1100

    .org 0x80
s3:
    ori  $1, $0, 0x0005     # 12:$1=0x00000005
    bgez $1, 2b             # 13:skip
    ori  $1, $0, 0x0006     # 14:$1=0x00000006
    ori  $1, $0, 0x1111
    ori  $1, $0, 0x1100

    .org 0x100
s4:
